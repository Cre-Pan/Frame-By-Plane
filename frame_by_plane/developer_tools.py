def _unregister_properties():
    for name in (
        "fbp_developer_tool_unlocked",
        "fbp_developer_test_running",
        "fbp_developer_test_progress",
        "fbp_developer_test_step",
        "fbp_developer_test_total",
        "fbp_developer_test_status",
        "fbp_developer_test_last_status",
    ):
        try:
            delattr(bpy.types.WindowManager, name)
        except FBP_DATA_IO_ERRORS:
            pass


def register():
    for cls in _CLASSES:
        bpy.utils.register_class(cls)
    _register_properties()


def unregister():
    _unregister_properties()
    for cls in reversed(_CLASSES):
        try:
            bpy.utils.unregister_class(cls)
        except FBP_DATA_IO_ERRORS:
            pass

    def _restore_original_context(self, context):
        window = getattr(context, "window", None)
        original = self._original_scene
        if window is not None and original is not None:
            try:
                if bpy.data.scenes.get(original.name) == original:
                    window.scene = original
                    original.frame_set(self._original_frame)
            except FBP_DATA_ERRORS:
                pass
        try:
            bpy.ops.object.select_all(action='DESELECT')
        except (AttributeError, RuntimeError):
            pass
        if original is not None:
            for name in self._original_selected_names or ():
                obj = original.objects.get(name)
                if obj is not None:
                    try:
                        obj.select_set(True)
                    except FBP_DATA_ERRORS:
                        pass
            active = self._original_active_object
            try:
                if active is not None and original.objects.get(active.name) == active:
                    context.view_layer.objects.active = active
            except FBP_DATA_ERRORS:
                pass
        # Re-enter only the common editable modes; unusual modes are left in
        # Object mode rather than risking a context-sensitive restoration error.
        mode_map = {
            "EDIT_MESH": "EDIT",
            "EDIT_CURVE": "EDIT",
            "EDIT_SURFACE": "EDIT",
            "EDIT_ARMATURE": "EDIT",
            "SCULPT": "SCULPT",
            "PAINT_WEIGHT": "WEIGHT_PAINT",
            "PAINT_VERTEX": "VERTEX_PAINT",
            "PAINT_TEXTURE": "TEXTURE_PAINT",
        }
        target_mode = mode_map.get(self._original_mode)
        if target_mode:
            try:
                if bpy.ops.object.mode_set.poll():
                    bpy.ops.object.mode_set(mode=target_mode)
            except (AttributeError, RuntimeError):
                pass

    def _remove_new_data(self):
        # Shape Mask caches and deferred mutation closures can contain names or
        # RNA wrappers derived from temporary Scenes. Retire both before freeing
        # IDs so nothing can resume against an obsolete Scene graph.
        try:
            from .object_masks import clear_object_mask_runtime_cache
            clear_object_mask_runtime_cache()
        except (ImportError, AttributeError, ReferenceError, RuntimeError, TypeError, ValueError):
            pass
        try:
            from .safe_tasks import clear_scheduled
            clear_scheduled()
        except (ImportError, AttributeError, ReferenceError, RuntimeError, TypeError, ValueError):
            pass

        # Remove test Scenes first so their owned collections are detached.
        original_scene_ptr = _id_pointer(self._original_scene) if self._original_scene is not None else None
        scene_snapshot = self._data_snapshot.get("scenes", set())
        for scene in tuple(bpy.data.scenes):
            pointer = _id_pointer(scene)
            if pointer in scene_snapshot or pointer == original_scene_ptr:
                continue
            try:
                bpy.data.scenes.remove(scene)
            except FBP_DATA_ERRORS:
                pass

        # Explicitly unlink every newly-created Object, even when generated
        # PointerProperties or collection links still report users. Shape Mask
        # helpers are removed first so no child retains a parent reference while
        # its generated plane/rig is freed.
        object_snapshot = self._data_snapshot.get("objects", set())
        new_objects = [
            item for item in tuple(bpy.data.objects)
            if _id_pointer(item) not in object_snapshot
        ]
        def _object_cleanup_priority(item):
            try:
                return 0 if bool(item.get("fbp_is_object_mask_helper", False)) else 1
            except FBP_DATA_ERRORS:
                return 1
        new_objects.sort(key=_object_cleanup_priority)
        for item in new_objects:
            try:
                bpy.data.objects.remove(item, do_unlink=True)
            except FBP_DATA_ERRORS:
                pass

        # Remove generated Collections after their Objects. Multiple passes deal
        # with nested children whose user counts fall only after a parent is freed.
        collection_snapshot = self._data_snapshot.get("collections", set())
        for _pass in range(4):
            changed = False
            for item in tuple(bpy.data.collections):
                if _id_pointer(item) in collection_snapshot:
                    continue
                try:
                    bpy.data.collections.remove(item, do_unlink=True)
                    changed = True
                except FBP_DATA_ERRORS:
                    continue
            if not changed:
                break

        # Remaining generated datablocks are safe to remove once no Object uses
        # them. Text cleanup remains restricted to known diagnostic names.
        for _pass in range(4):
            changed = False
            for name in _CLEANUP_COLLECTIONS:
                if name in {"objects", "collections"}:
                    continue
                collection = getattr(bpy.data, name, None)
                if collection is None:
                    continue
                before = self._data_snapshot.get(name, set())
                for item in tuple(collection):
                    if _id_pointer(item) in before:
                        continue
                    try:
                        users = int(getattr(item, "users", 0) or 0)
                    except FBP_DATA_ERRORS:
                        continue
                    if name == "texts":
                        item_name = str(getattr(item, "name", "") or "")
                        is_test_text = (
                            item_name in _INTERMEDIATE_REPORT_NAMES
                            or item_name.startswith("FBP_Persistence_Baseline")
                            or item_name == _REPORT_NAME
                        )
                        if not is_test_text:
                            continue
                    elif users > 0:
                        continue
                    try:
                        collection.remove(item)
                        changed = True
                    except FBP_DATA_ERRORS:
                        continue
            if not changed:
                break

        try:
            from .object_masks import clear_object_mask_runtime_cache
            clear_object_mask_runtime_cache()
        except (ImportError, AttributeError, ReferenceError, RuntimeError, TypeError, ValueError):
            pass

    def _restore_scene_names(self):
        for scene, original_name in reversed(self._scene_backups or ()):
            try:
                if scene.name in bpy.data.scenes:
                    occupant = bpy.data.scenes.get(original_name)
                    if occupant is not None and occupant != scene:
                        bpy.data.scenes.remove(occupant)
                    scene.name = original_name
            except FBP_DATA_ERRORS:
                pass

    def _restore_intermediate_reports(self):
        for name, state in (self._report_snapshot or {}).items():
            text = bpy.data.texts.get(name)
            if state is None:
                if text is not None:
                    try:
                        bpy.data.texts.remove(text)
                    except FBP_DATA_ERRORS:
                        pass
                continue
            content, fake_user = state
            if text is None:
                text = bpy.data.texts.new(name)
            try:
                text.clear()
                text.write(content)
                text.use_fake_user = bool(fake_user)
            except FBP_DATA_ERRORS:
                pass

    def _build_final_report(self):
        total_elapsed = time.perf_counter() - self._start_time
        failures = [record for record in self._records if record["status"] == "FAIL"]
        warnings = [record for record in self._records if record["status"] == "WARNING"]
        passes = [record for record in self._records if record["status"] == "PASS"]
        overall = "FAIL" if failures else ("WARNING" if warnings or self._cancelled else "PASS")
        lines = [
            "Frame By Plane — Autonomous Developer Test",
            "===========================================",
            f"Generated: {datetime.now().isoformat(timespec='seconds')}",
            f"Frame By Plane: {FBP_VERSION_STRING}",
            f"Blender: {bpy.app.version_string}",
            f"Platform: {platform.platform()}",
            f"Python: {platform.python_version()}",
            f"Duration: {total_elapsed:.2f} seconds",
            f"Result: {overall}",
            "",
            "Coverage summary",
            "----------------",
            f"Registered FBP operators discovered: {self._operator_count}",
            f"Registered effects: {self._effect_count}",
            f"Passed sections: {len(passes)}",
            f"Warning sections: {len(warnings)}",
            f"Failed sections: {len(failures)}",
            f"Operator poll exceptions: {len(self._poll_errors or ())}",
            "",
            "Automated sections",
            "------------------",
        ]
        for index, record in enumerate(self._records, 1):
            duration = f" · {record['duration']:.2f}s" if record["duration"] > 0.0 else ""
            lines.append(f"{index:02d}. [{record['status']}] {record['name']} — {record['summary']}{duration}")
            for detail in record["details"]:
                lines.append(f"    - {detail}")

        lines.extend((
            "",
            "Manual release checks still required",
            "------------------------------------",
        ))
        lines.extend(f"- {item}" for item in _MANUAL_CHECKS)
        lines.extend((
            "",
            "Interpretation",
            "--------------",
            "PASS means the autonomous synthetic regression suite completed without a detected blocker.",
            "WARNING means the suite completed but found a non-blocking diagnostic or was cancelled.",
            "FAIL means at least one automated section found a blocker or could not complete.",
            "Manual checks are intentionally excluded from the automated result and must be completed before a public release.",
            "",
            "Result",
            "------",
            overall,
        ))
        return lines, overall

    def _finish(self, context):
        if self._finished:
            return
        self._finished = True
        # Keep all managed timers paused until every temporary ID and report has
        # been finalized. The finally block guarantees recovery after any cleanup
        # or clipboard error and adds one idle event-loop grace period.
        fbp_runtime_set("fbp_pause_managed_timers", True)
        try:
            self._finish_impl(context)
        finally:
            try:
                from .safe_tasks import clear_scheduled
                clear_scheduled()
            except (ImportError, AttributeError, ReferenceError, RuntimeError, TypeError, ValueError):
                pass
            try:
                from .object_masks import clear_object_mask_runtime_cache
                clear_object_mask_runtime_cache()
            except (ImportError, AttributeError, ReferenceError, RuntimeError, TypeError, ValueError):
                pass
            try:
                wm = getattr(context, "window_manager", None)
                if wm is not None:
                    wm.fbp_developer_test_running = False
            except FBP_DATA_ERRORS:
                pass
            fbp_runtime_set("fbp_managed_timers_resume_after", time.monotonic() + 1.0)
            fbp_runtime_set("fbp_pause_managed_timers", False)

    def _finish_impl(self, context):
        wm = getattr(context, "window_manager", None)
        if self._timer is not None and wm is not None:
            try:
                wm.event_timer_remove(self._timer)
            except (ReferenceError, RuntimeError):
                pass
            self._timer = None

        # The original Scene must be active before generated Scenes are removed.
        self._restore_original_context(context)
        self._remove_new_data()
        self._restore_scene_names()
        self._restore_intermediate_reports()
        for path in self._temp_paths or ():
            try:
                shutil.rmtree(path, ignore_errors=True)
            except OSError:
                pass

        lines, overall = self._build_final_report()
        original_scene = self._original_scene
        text = write_diagnostic_report(
            original_scene,
            _REPORT_NAME,
            lines,
            summary=f"Developer Tool · {overall}",
            status="PASS" if overall == "PASS" else "WARNING",
        )
        report_text = "\n".join(lines) if text is None else _safe_text_string(text)
        clipboard_ok = True
        try:
            context.window_manager.clipboard = report_text
        except FBP_DATA_ERRORS as exc:
            fbp_warn("Could not copy the Developer Tool report", exc)
            clipboard_ok = False
            overall = "FAIL"
            lines.extend((
                "",
                "Clipboard error",
                "---------------",
                f"{type(exc).__name__}: {exc}",
            ))
            text = write_diagnostic_report(
                original_scene,
                _REPORT_NAME,
                lines,
                summary="Developer Tool · FAIL",
                status="WARNING",
            )

        if wm is not None:
            wm.fbp_developer_test_running = False
            wm.fbp_developer_test_progress = 1.0
            wm.fbp_developer_test_step = int(getattr(wm, "fbp_developer_test_total", 0) or 0)
            wm.fbp_developer_test_status = (
                f"{overall} · report copied to clipboard"
                if clipboard_ok else "FAIL · report saved in Blender Text Editor"
            )
            wm.fbp_developer_test_last_status = overall
        _tag_redraw()
        self.report(
            {'INFO'} if overall == "PASS" else {'WARNING'},
            (
                f"Developer Tool {overall} — report copied to clipboard"
                if clipboard_ok else "Developer Tool FAIL — report saved in Blender Text Editor"
            ),
        )

    def cancel(self, context):
        self._cancelled = True
        self._finish(context)


